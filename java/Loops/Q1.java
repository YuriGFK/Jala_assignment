package Loops;
//Write a program to print “Bright IT Career” ten times using for loop
public class Q1 {
	public static void main(String[] args) {
		int count = 0;
		for(int i=0; i<10; i++) {
			System.out.println("Bright IT Career");
			count++;
		}
		System.out.println("No of times Bright IT Career printed: "+ count);
	}

}
