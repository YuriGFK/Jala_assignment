package Strings;

public class Q14 {
//Converting to uppercase and lowercase
	public static void main(String[] args) {
		//Converting to uppercase and lowercase
		String string = "JALA ACADEMY";
		String message = new String("jala technologies");
		String string1 = string.toLowerCase();
		String message1 = message.toUpperCase();
		System.out.println(string);
		System.out.println("After using toLowerCase() function");
		System.out.println(string1);
		System.out.println(message);
		System.out.println("After using toUpperCase() function");
		System.out.println(message1);
				

	}

}
